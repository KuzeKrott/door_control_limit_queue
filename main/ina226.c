#include "ina226.h"
#include "driver/i2c.h"   /* Функции i2c_master_cmd_begin, i2c_cmd_link_create и т.д. */
#include "esp_log.h"       /* ESP_LOGI, ESP_LOGE — логирование в монитор порта */
#include "freertos/FreeRTOS.h"
#include "freertos/task.h" /* vTaskDelay — нужен для паузы после сброса */

/* ─── I2C-адрес датчика ────────────────────────────────────────────────────────
 *
 * INA226 имеет две ноги A0 и A1, которые задают адрес.
 * Если обе ноги подключены к GND — адрес 0x40.
 * Это позволяет повесить на одну шину до 16 датчиков INA226 с разными адресами.
 *
 * Таблица адресов (из datasheet):
 *   A1=GND, A0=GND → 0x40   ← наш случай
 *   A1=GND, A0=VCC → 0x41
 *   A1=VCC, A0=GND → 0x44
 *   ...и так далее до 0x4F
 */
#define INA226_ADDR      0x40

/* Тот же I2C-порт (NUM_0), на котором уже висит MCP4725.
 * Оба устройства спокойно сосуществуют на одной шине — у каждого свой адрес. */
#define I2C_PORT         I2C_NUM_0

/* ─── Адреса регистров датчика ────────────────────────────────────────────────
 *
 * Внутри INA226 есть несколько 16-битных регистров.
 * Чтобы обратиться к нужному, мы сначала отправляем его номер (адрес регистра),
 * а потом читаем или записываем 2 байта данных.
 *
 * REG_CONFIG  (0x00) — регистр настройки: как часто мерить, сколько усреднять
 * REG_BUS_V   (0x02) — напряжение на шине (на нагрузке), в единицах 1.25 мВ/бит
 * REG_CURRENT (0x04) — вычисленный ток, в единицах current_lsb А/бит
 * REG_CALIB   (0x05) — коэффициент калибровки, задаём сами (см. формулу ниже)
 */
#define REG_CONFIG       0x00
#define REG_BUS_V        0x02
#define REG_CURRENT      0x04
#define REG_CALIB        0x05

static const char *TAG = "ina226";

/* ─── Цена одного бита в регистре тока ───────────────────────────────────────
 *
 * INA226 хранит ток не в амперах, а в целых числах (битах).
 * current_lsb — это то, сколько ампер "весит" один бит.
 *
 * Пример: если current_lsb = 0.001 (1 мА/бит), и регистр прочитал 350,
 * то реальный ток = 350 × 0.001 = 0.35 А.
 *
 * Значение задаётся при инициализации и сохраняется здесь,
 * чтобы функция чтения знала, как перевести биты в амперы.
 */
static float s_current_lsb = 0.001f;


/* ═══════════════════════════════════════════════════════════════════════════════
 * ЗАПИСЬ В РЕГИСТР ДАТЧИКА
 *
 * I2C-транзакция записи выглядит так (каждый шаг = один байт на шине):
 *
 *  START → [адрес датчика + бит WRITE] → [номер регистра] → [байт 1] → [байт 2] → STOP
 *
 * INA226 ожидает данные старшим байтом вперёд (big-endian):
 * сначала старший байт (MSB), потом младший байт (LSB).
 * ═══════════════════════════════════════════════════════════════════════════════ */
static esp_err_t reg_write(uint8_t reg, uint16_t val)
{
    /* Упаковываем всё в буфер из 3 байт:
     *   buf[0] = номер регистра (например 0x00 для CONFIG)
     *   buf[1] = старший байт значения  (val >> 8 отбрасывает младшие 8 бит)
     *   buf[2] = младший байт значения  (val & 0xFF оставляет только младшие 8 бит)
     *
     * Пример: val = 0x1C27 (0001 1100 0010 0111)
     *   val >> 8       = 0x1C  (старший байт)
     *   val & 0xFF     = 0x27  (младший байт)
     */
    uint8_t buf[3] = { reg, (uint8_t)(val >> 8), (uint8_t)(val & 0xFF) };

    /* Создаём «список команд» — это очередь I2C-операций в памяти.
     * Пока ничего на шину не отправляется, просто формируем пакет. */
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();

    /* Условие START — сигнал «начало передачи» на шине I2C */
    i2c_master_start(cmd);

    /* Адресный байт: 7-битный адрес датчика сдвигаем влево на 1 бит,
     * освобождая место для бита направления (0 = запись, 1 = чтение).
     * I2C_MASTER_WRITE = 0, поэтому OR с ним ничего не меняет, но читается понятнее.
     *
     * Пример: INA226_ADDR = 0x40 = 0100 0000
     *   << 1 → 1000 0000
     *   | 0  → 1000 0000 = 0x80  (бит 0 = 0 → запись)
     *
     * Второй аргумент true = ждать подтверждения (ACK) от датчика */
    i2c_master_write_byte(cmd, (INA226_ADDR << 1) | I2C_MASTER_WRITE, true);

    /* Отправляем все 3 байта буфера (регистр + 2 байта данных) */
    i2c_master_write(cmd, buf, sizeof(buf), true);

    /* Условие STOP — сигнал «конец передачи» */
    i2c_master_stop(cmd);

    /* Только здесь пакет реально уходит на шину.
     * pdMS_TO_TICKS(100) — таймаут 100 мс: если датчик не отвечает, вернём ошибку */
    esp_err_t ret = i2c_master_cmd_begin(I2C_PORT, cmd, pdMS_TO_TICKS(100));

    /* Освобождаем память списка команд (обязательно, иначе утечка памяти) */
    i2c_cmd_link_delete(cmd);

    return ret;
}


/* ═══════════════════════════════════════════════════════════════════════════════
 * ЧТЕНИЕ ИЗ РЕГИСТРА ДАТЧИКА
 *
 * Чтение из I2C-устройства сложнее записи — требует "повторного старта":
 *
 *  START → [адрес + WRITE] → [номер регистра]        ← говорим: "хочу читать вот этот регистр"
 *  START → [адрес + READ]  → [байт 1] → [байт 2] → STOP  ← читаем 2 байта ответа
 *
 * Два START подряд без STOP между ними — это и есть "повторный старт" (repeated start).
 * Он нужен потому что после указания регистра нужно переключиться с WRITE на READ.
 * ═══════════════════════════════════════════════════════════════════════════════ */
static esp_err_t reg_read(uint8_t reg, uint16_t *out)
{
    uint8_t buf[2] = {0}; /* Сюда датчик положит 2 байта ответа */

    i2c_cmd_handle_t cmd = i2c_cmd_link_create();

    /* Фаза 1: указываем датчику, какой регистр хотим прочитать */
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (INA226_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, reg, true); /* отправляем только номер регистра (1 байт) */

    /* Фаза 2: повторный старт, переключаемся в режим чтения */
    i2c_master_start(cmd);

    /* Тот же адрес датчика, но теперь бит READ = 1:
     *   0x40 << 1 = 0x80 = 1000 0000
     *   | 1       = 1000 0001 = 0x81  (бит 0 = 1 → чтение) */
    i2c_master_write_byte(cmd, (INA226_ADDR << 1) | I2C_MASTER_READ, true);

    /* Читаем 2 байта. I2C_MASTER_LAST_NACK означает:
     * последний байт принять с NACK (не-подтверждение) — это стандартный способ
     * сказать датчику "всё, больше не нужно, заканчивай" */
    i2c_master_read(cmd, buf, 2, I2C_MASTER_LAST_NACK);

    i2c_master_stop(cmd);
    esp_err_t ret = i2c_master_cmd_begin(I2C_PORT, cmd, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(cmd);

    if (ret == ESP_OK) {
        /* Собираем 16-битное число из двух байт (big-endian):
         * buf[0] — старший байт, сдвигаем на 8 позиций влево
         * buf[1] — младший байт, добавляем через OR
         *
         * Пример: buf[0]=0x01, buf[1]=0x5E
         *   (0x01 << 8) = 0x0100
         *   | 0x5E      = 0x015E = 350 в десятичной */
        *out = ((uint16_t)buf[0] << 8) | buf[1];
    }

    return ret;
}


/* ═══════════════════════════════════════════════════════════════════════════════
 * ИНИЦИАЛИЗАЦИЯ ДАТЧИКА
 *
 * Параметры:
 *   shunt_ohms    — сопротивление шунтового резистора в Омах (например 0.1)
 *   current_lsb_a — цена одного бита тока в Амперах (например 0.001 = 1 мА/бит)
 *
 * Последовательность:
 *   1. Сброс — возвращает все регистры к заводским значениям
 *   2. CONFIG — задаём режим работы (усреднение, скорость измерения)
 *   3. CALIB  — задаём масштабный коэффициент для расчёта тока
 * ═══════════════════════════════════════════════════════════════════════════════ */
esp_err_t ina226_init(float shunt_ohms, float current_lsb_a)
{
    /* Сохраняем current_lsb — понадобится при каждом чтении тока */
    s_current_lsb = current_lsb_a;

    /* ── Шаг 1: Программный сброс ──────────────────────────────────────────────
     *
     * Запись 0x8000 в регистр CONFIG:
     *   0x8000 = 1000 0000 0000 0000
     *   Бит 15 (RST) = 1 → датчик сбрасывает все свои регистры
     *
     * Зачем: гарантируем чистое состояние при каждом старте,
     * даже если ESP перезагрузилась, а INA226 — нет (питание не прерывалось).
     */
    esp_err_t ret = reg_write(REG_CONFIG, 0x8000);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Сброс не удался: %s", esp_err_to_name(ret));
        return ret;
    }

    /* После сброса датчику нужно немного времени.
     * 10 мс — с запасом, datasheet не требует столько, но безопаснее. */
    vTaskDelay(pdMS_TO_TICKS(10));

    /* ── Шаг 2: Регистр CONFIG ─────────────────────────────────────────────────
     *
     * Регистр CONFIG — 16 бит, каждая группа бит задаёт один параметр:
     *
     * Бит 15:    RST  — сброс (пишем 0, иначе снова сбросится)
     * Биты 14:12 — зарезервированы, должны быть 100 (заводское значение, не трогаем)
     * Биты 11:9  — AVG  — количество усреднений одного измерения
     * Биты 8:6   — VBUSCT — время одного измерения напряжения шины
     * Биты 5:3   — VSHCT  — время одного измерения напряжения на шунте
     * Биты 2:0   — MODE   — режим работы
     *
     * Выбранные значения:
     *
     *   AVG = 0b010 = 2 → AVG_16 (усредняем 16 измерений)
     *     Зачем: ток двигателя имеет пульсации. 16 усреднений сглаживают шум.
     *     Варианты: 000=1, 001=4, 010=16, 011=64, 100=128, 101=256, 110=512, 111=1024
     *
     *   VBUSCT = 0b100 = 4 → 1.1 мс на одно измерение напряжения шины
     *     Варианты: 000=140мкс, 001=204мкс, 010=332мкс, 011=588мкс,
     *               100=1.1мс, 101=2.116мс, 110=4.156мс, 111=8.244мс
     *
     *   VSHCT  = 0b100 = 4 → 1.1 мс на одно измерение шунтового напряжения
     *     (те же варианты что у VBUSCT)
     *
     *   MODE = 0b111 = 7 → SHUNT_BUS_CONT — непрерывное измерение и шунта, и шины
     *     Датчик меряет постоянно, результат всегда актуален в регистрах.
     *     Варианты: 000=выкл, 001=шунт однократно, 010=шина однократно,
     *               011=шунт+шина однократно, 100=АЦП выкл,
     *               101=шунт непрерывно, 110=шина непрерывно, 111=всё непрерывно
     *
     * Итоговое значение регистра:
     *   Биты 11:9 = 010, сдвиг на 9:  0b010 << 9 = 0000 0100 0000 0000 = 0x0400
     *   Биты 8:6  = 100, сдвиг на 6:  0b100 << 6 = 0000 0001 0000 0000 = 0x0100
     *   Биты 5:3  = 100, сдвиг на 3:  0b100 << 3 = 0000 0000 0010 0000 = 0x0020
     *   Биты 2:0  = 111, без сдвига:  0b111      = 0000 0000 0000 0111 = 0x0007
     *
     *   OR всех вместе: 0x0400 | 0x0100 | 0x0020 | 0x0007 = 0x0527
     *
     * Примечание: биты 14:12 мы не трогаем (остаются 0), датчик их игнорирует при записи.
     */
    uint16_t config = (0b010 << 9) | (0b100 << 6) | (0b100 << 3) | 0b111;
    ret = reg_write(REG_CONFIG, config);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Конфигурация не удалась: %s", esp_err_to_name(ret));
        return ret;
    }

    /* ── Шаг 3: Регистр CALIBRATION ────────────────────────────────────────────
     *
     * INA226 не знает сопротивление твоего шунта и не знает желаемый масштаб тока.
     * Регистр CALIB — это коэффициент, который ты ему задаёшь,
     * чтобы он правильно пересчитывал падение напряжения на шунте в ток.
     *
     * Формула из datasheet TI:
     *
     *   CAL = 0.00512 / (current_lsb × R_shunt)
     *
     * Откуда взялось 0.00512:
     *   Это внутренняя константа INA226 (фиксированная точность АЦП шунта).
     *   Датчик измеряет шунтовое напряжение с разрядностью 2.5 мкВ/бит.
     *   0.00512 = 2.5e-6 × 2048 — так работает внутренняя арифметика чипа.
     *
     * Пример расчёта (наш случай: шунт 0.1 Ом, lsb = 1 мА = 0.001 А):
     *
     *   CAL = 0.00512 / (0.001 × 0.1)
     *       = 0.00512 / 0.0001
     *       = 51.2 → округляем → 51
     *
     * Проверка: что означает CAL=51?
     *   Если на шунте 0.1 Ом течёт 1 А → падение = 0.1 В = 100 000 мкВ
     *   Датчик измеряет 100 000 / 2.5 = 40 000 единиц шунтового напряжения
     *   Датчик вычисляет ток: raw_current = (40 000 × 51) / 2048 ≈ 996 бит
     *   Мы умножаем: 996 × 0.001 А = 0.996 А ≈ 1 А ✓
     *
     * Замечание про current_lsb:
     *   Выбирается из диапазона измеряемых токов. Если максимальный ток = 2 А,
     *   то current_lsb = 2 / 32768 ≈ 0.000061 А (0.061 мА/бит) — максимальная точность.
     *   Мы используем 1 мА/бит для простоты — точность достаточная для защиты двери.
     *
     * + 0.5f в конце — это округление float→int (аналог round()).
     * Без него, например, 51.9 обрезалось бы до 51 вместо 52.
     */
    float cal_f = 0.00512f / (current_lsb_a * shunt_ohms);
    uint16_t cal = (uint16_t)(cal_f + 0.5f);

    /* Защита от нуля: если cal вышел 0 (что невозможно при нормальных параметрах,
     * но на случай ошибки пользователя), ставим 1 — иначе датчик выдаст мусор */
    if (cal == 0) cal = 1;

    ret = reg_write(REG_CALIB, cal);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Калибровка не удалась: %s", esp_err_to_name(ret));
        return ret;
    }

    ESP_LOGI(TAG, "INA226 инициализирован: шунт=%.3f Ом, lsb=%.4f А, CAL=%u",
             shunt_ohms, current_lsb_a, cal);
    return ESP_OK;
}


/* ═══════════════════════════════════════════════════════════════════════════════
 * ЧТЕНИЕ ТОКА
 *
 * Регистр CURRENT (0x04) — 16-битное ЗНАКОВОЕ целое (int16_t).
 * Знаковое — потому что ток может быть отрицательным (если ток течёт в обратную сторону).
 * Для нас отрицательного тока быть не должно, но датчик так устроен.
 * ═══════════════════════════════════════════════════════════════════════════════ */
float ina226_read_current_a(void)
{
    uint16_t raw = 0;

    /* Если I2C-чтение не удалось — возвращаем 0, а не мусор */
    if (reg_read(REG_CURRENT, &raw) != ESP_OK) return 0.0f;

    /* (int16_t)raw — принудительно интерпретируем 16 бит как знаковое число.
     * Если бит 15 = 1, то число станет отрицательным (дополнительный код).
     * Это важно: без этого приведения значение 0xFFFF означало бы 65535,
     * а с ним — (-1), что и есть «почти ноль» для знакового регистра.
     *
     * Умножаем на current_lsb — переводим «биты» в «амперы».
     * Пример: raw=350, current_lsb=0.001 → 350 × 0.001 = 0.35 А */
    return (int16_t)raw * s_current_lsb;
}


/* ═══════════════════════════════════════════════════════════════════════════════
 * ЧТЕНИЕ НАПРЯЖЕНИЯ ШИНЫ
 *
 * Регистр BUS_V (0x02) — 16-битное БЕЗЗНАКОВОЕ число.
 * Цена одного бита зафиксирована в datasheet: 1.25 мВ/бит (нельзя изменить).
 *
 * Это напряжение на нагрузке (на актуаторе), а не на шунте.
 * Полезно для диагностики: можно убедиться, что питание подключено.
 * ═══════════════════════════════════════════════════════════════════════════════ */
float ina226_read_bus_voltage_v(void)
{
    uint16_t raw = 0;
    if (reg_read(REG_BUS_V, &raw) != ESP_OK) return 0.0f;

    /* 1.25e-3f = 0.00125 = 1.25 мВ в вольтах.
     * Пример: raw=19200 → 19200 × 0.00125 = 24.0 В (питание 24 В) */
    return raw * 1.25e-3f;
}
